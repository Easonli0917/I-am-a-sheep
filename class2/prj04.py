#######################匯入模組#######################
# 匯入tkinter模組
from tkinter import *

#######################建立視窗########################
# 建立視窗
windows = Tk()
# 設置視窗名稱
windows.title("I-am-a-sheep")
#######################運行應用程式########################
# 開始執行主迴圈等待用戶操
windows.mainloop()
#######################創建畫布############################
# 創建畫布
canvas = Canvas(windows, width=600, height=600, bg="white")
canvas.pack()
